    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String userName = user.getText();
         String passWord = password.getText();
         if(userName.equals("admin")&& passWord.equals("12345678")){
             managerView.setVisible(true);  
             dispose();
         }else if(userName.equals("cashier")&& passWord.equals("12345678")){
             managerView.setVisible(true);  
             managerView.hideJPanel2();
              managerView.hideJPanel1();
             dispose();
         }
         else if(userName.equals("")|| passWord.equals("")){
             JOptionPane.showMessageDialog(null,"User Name or Password can't be empty");
         }
         else{
             JOptionPane.showMessageDialog(null,"Incorrect UserName or Password");
         }
         
    }//GEN-LAST:event_jButton1ActionPerformed

